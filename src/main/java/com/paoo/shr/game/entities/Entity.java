package com.paoo.shr.game.entities;

import com.paoo.shr.engine.graphics.Drawable;
import com.paoo.shr.engine.scenes.Updateable;
import com.paoo.shr.game.entities.collisions.Collidable;
import com.paoo.shr.game.entities.collisions.HitBox;
import com.paoo.shr.math.Rect;
import com.paoo.shr.math.Vector2;
import javafx.scene.image.Image;

/**
 * Clasa de baza pentru toate entitatile (player, mobi, blockuri, npc, etc.)
 */
public abstract class Entity implements Drawable, Collidable, Updateable {

    /*
    ┌────────────────────────────────────┐
    │   ____   _          _    _         │
    │  / ___| | |_  __ _ | |_ (_)  ___   │
    │  \___ \ | __|/ _` || __|| | / __|  │
    │   ___) || |_| (_| || |_ | || (__   │
    │  |____/  \__|\__,_| \__||_| \___|  │
    └────────────────────────────────────┘
     */


    /*
    ┌─────────────────────────────────────────────────────┐
    │   __  __                   _                        │
    │  |  \/  |  ___  _ __ ___  | |__    ___  _ __  ___   │
    │  | |\/| | / _ \| '_ ` _ \ | '_ \  / _ \| '__|/ __|  │
    │  | |  | ||  __/| | | | | || |_) ||  __/| |   \__ \  │
    │  |_|  |_| \___||_| |_| |_||_.__/  \___||_|   |___/  │
    └─────────────────────────────────────────────────────┘
    */
    protected Vector2 pos;
    protected Vector2 size;
    protected HitBox hitBox;
    /*
    ┌───────────────────────────────────────────────────────────────────────────┐
    │    ____                    _                       _                      │
    │   / ___| ___   _ __   ___ | |_  _ __  _   _   ___ | |_  ___   _ __  ___   │
    │  | |    / _ \ | '_ \ / __|| __|| '__|| | | | / __|| __|/ _ \ | '__|/ __|  │
    │  | |___| (_) || | | |\__ \| |_ | |   | |_| || (__ | |_| (_) || |   \__ \  │
    │   \____|\___/ |_| |_||___/ \__||_|    \__,_| \___| \__|\___/ |_|   |___/  │
    └───────────────────────────────────────────────────────────────────────────┘
    */
    
    /*
    ┌───────────────────────────────────────────────────┐
    │     __  __        _    _                 _        │
    │    |  \/  |  ___ | |_ | |__    ___    __| | ___   │
    │    | |\/| | / _ \| __|| '_ \  / _ \  / _` |/ __|  │
    │    | |  | ||  __/| |_ | | | || (_) || (_| |\__ \  │
    │    |_|  |_| \___| \__||_| |_| \___/  \__,_||___/  │
    └───────────────────────────────────────────────────┘
    */
    protected void updateHitBox() {
        this.hitBox.moveTo(this.pos);
    }

    /*
    ┌───────────────────────────────────────────┐
    │    ____        _    _                     │
    │   / ___|  ___ | |_ | |_  ___  _ __  ___   │
    │  | |  _  / _ \| __|| __|/ _ \| '__|/ __|  │
    │  | |_| ||  __/| |_ | |_|  __/| |   \__ \  │
    │   \____| \___| \__| \__|\___||_|   |___/  │
    └───────────────────────────────────────────┘
    */
    public Vector2 getPosition() {
        return pos.shallowCopy();
    }
    public float getY() {
        return pos.getY();
    }

    public Vector2 getSize() {
        return size.shallowCopy();
    }

    /*
    ┌───────────────────────────────────────────┐
    │   ____         _    _                     │
    │  / ___|   ___ | |_ | |_  ___  _ __  ___   │
    │  \___ \  / _ \| __|| __|/ _ \| '__|/ __|  │
    │   ___) ||  __/| |_ | |_|  __/| |   \__ \  │
    │  |____/  \___| \__| \__|\___||_|   |___/  │
    └───────────────────────────────────────────┘
    */

}
